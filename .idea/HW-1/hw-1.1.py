"""
    Сортировка списка целых чисел в порядке убывания с использованием сортировки пузырьком.

    Аргументы:
    numbers: Список целых чисел, который требуется отсортировать.

    Возвращает:
    None. Список numbers сортируется "на месте", и ничего не возвращается.
"""

def sort_list_imperative(numbers):
    n = len(numbers)
    for i in range(n):
        for j in range(0, n-i-1):
            if numbers[j] < numbers[j+1]:
                numbers[j], numbers[j+1] = numbers[j+1], numbers[j]

